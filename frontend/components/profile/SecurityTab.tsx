'use client';

import { useState } from 'react';
import { Copy, Check } from 'lucide-react';
import { api } from '@/lib/api/api';

interface UserProfile {
  twoFactorEnabled?: boolean;
  email?: string;
}

interface SecuritySectionProps {
  profile: UserProfile | null;
  onProfileUpdate?: (p: UserProfile) => void;
}

export function SecuritySection({ profile, onProfileUpdate }: SecuritySectionProps) {
  // Смена пароля
  const [currentPassword, setCurrentPassword] = useState('');
  const [newPassword, setNewPassword] = useState('');
  const [confirmPassword, setConfirmPassword] = useState('');
  const [passwordSaving, setPasswordSaving] = useState(false);
  const [passwordError, setPasswordError] = useState<string | null>(null);
  const [passwordSuccess, setPasswordSuccess] = useState(false);

  // 2FA
  const [step2FA, setStep2FA] = useState<'idle' | 'qr' | 'verify'>('idle');
  const [qrCode, setQrCode] = useState<string | null>(null);
  const [backupCodes, setBackupCodes] = useState<string[]>([]);
  const [verifyCode, setVerifyCode] = useState('');
  const [enable2FALoading, setEnable2FALoading] = useState(false);
  const [verify2FALoading, setVerify2FALoading] = useState(false);
  const [disable2FAPassword, setDisable2FAPassword] = useState('');
  const [disable2FACode, setDisable2FACode] = useState('');
  const [disable2FALoading, setDisable2FALoading] = useState(false);
  const [twoFAError, setTwoFAError] = useState<string | null>(null);
  const [twoFASuccess, setTwoFASuccess] = useState<string | null>(null);
  const [copiedCode, setCopiedCode] = useState<number | null>(null);

  const handleChangePassword = async (e: React.FormEvent) => {
    e.preventDefault();
    setPasswordError(null);
    setPasswordSuccess(false);
    if (newPassword !== confirmPassword) {
      setPasswordError('Пароли не совпадают');
      return;
    }
    if (newPassword.length < 8) {
      setPasswordError('Новый пароль должен быть не менее 8 символов');
      return;
    }
    setPasswordSaving(true);
    try {
      await api('/api/user/change-password', {
        method: 'POST',
        body: JSON.stringify({
          currentPassword,
          newPassword,
        }),
      });
      setPasswordSuccess(true);
      setCurrentPassword('');
      setNewPassword('');
      setConfirmPassword('');
      setTimeout(() => setPasswordSuccess(false), 3000);
    } catch (err: unknown) {
      const e = err as { message?: string; response?: { data?: { message?: string } } };
      setPasswordError(e.response?.data?.message || e.message || 'Ошибка смены пароля');
    } finally {
      setPasswordSaving(false);
    }
  };

  const handleEnable2FA = async () => {
    setTwoFAError(null);
    setEnable2FALoading(true);
    try {
      const res = await api<{ qrCode: string; backupCodes: string[] }>('/api/user/2fa/enable', {
        method: 'POST',
      });
      setQrCode(res.qrCode);
      setBackupCodes(res.backupCodes);
      setStep2FA('qr');
    } catch (err: unknown) {
      const e = err as { message?: string };
      setTwoFAError(e.message || 'Ошибка включения 2FA');
    } finally {
      setEnable2FALoading(false);
    }
  };

  const handleVerify2FA = async (e: React.FormEvent) => {
    e.preventDefault();
    setTwoFAError(null);
    setVerify2FALoading(true);
    try {
      await api('/api/user/2fa/verify', {
        method: 'POST',
        body: JSON.stringify({ code: verifyCode }),
      });
      setTwoFASuccess('2FA успешно включена');
      setStep2FA('idle');
      setVerifyCode('');
      setQrCode(null);
      setBackupCodes([]);
      const updated = { ...profile, twoFactorEnabled: true };
      onProfileUpdate?.(updated);
      document.dispatchEvent(new CustomEvent('profile-updated', { detail: updated }));
      setTimeout(() => setTwoFASuccess(null), 3000);
    } catch (err: unknown) {
      const e = err as { message?: string };
      setTwoFAError(e.message || 'Неверный код. Попробуйте снова.');
    } finally {
      setVerify2FALoading(false);
    }
  };

  const handleDisable2FA = async (e: React.FormEvent) => {
    e.preventDefault();
    setTwoFAError(null);
    setTwoFASuccess(null);
    if (!disable2FAPassword || !disable2FACode) {
      setTwoFAError('Введите пароль и код из приложения');
      return;
    }
    setDisable2FALoading(true);
    try {
      await api('/api/user/2fa/disable', {
        method: 'POST',
        body: JSON.stringify({ password: disable2FAPassword, code: disable2FACode }),
      });
      setTwoFASuccess('2FA отключена');
      setDisable2FAPassword('');
      setDisable2FACode('');
      const updated = { ...profile, twoFactorEnabled: false };
      onProfileUpdate?.(updated);
      document.dispatchEvent(new CustomEvent('profile-updated', { detail: updated }));
      setTimeout(() => setTwoFASuccess(null), 3000);
    } catch (err: unknown) {
      const e = err as { message?: string; response?: { data?: { message?: string } } };
      setTwoFAError(e.response?.data?.message || e.message || 'Ошибка отключения 2FA');
    } finally {
      setDisable2FALoading(false);
    }
  };

  const handleCopyBackupCode = (code: string, idx: number) => {
    navigator.clipboard.writeText(code);
    setCopiedCode(idx);
    setTimeout(() => setCopiedCode(null), 2000);
  };

  const cancel2FASetup = () => {
    setStep2FA('idle');
    setQrCode(null);
    setBackupCodes([]);
    setVerifyCode('');
    setTwoFAError(null);
  };

  const inputClass = 'w-full px-3 py-2 sm:px-4 sm:py-3 rounded-lg sm:rounded-xl bg-white/5 border border-white/10 text-sm sm:text-base text-white placeholder-white/30 focus:outline-none focus:ring-2 focus:ring-[#3347ff]/50 focus:border-[#3347ff]/50';
  const labelClass = 'block text-xs sm:text-sm font-medium text-white/70 mb-1.5 sm:mb-2';

  return (
    <div className="w-full space-y-6">
      {/* Смена пароля — карточка на всю ширину */}
      <section className="rounded-lg sm:rounded-xl bg-[#0a1635] border border-white/10 p-4 sm:p-6">
        <div className="mb-4 sm:mb-6">
          <h2 className="text-base sm:text-lg font-semibold text-white">Смена пароля</h2>
          <p className="text-sm text-white/50 mt-0.5">Обновите пароль для защиты аккаунта</p>
        </div>
        <form onSubmit={handleChangePassword} className="w-full">
          <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-3 gap-4 sm:gap-6">
            <div>
              <label className={labelClass}>Текущий пароль</label>
              <input
                type="password"
                value={currentPassword}
                onChange={(e) => setCurrentPassword(e.target.value)}
                placeholder="••••••••"
                className={inputClass}
                maxLength={128}
                required
              />
            </div>
            <div>
              <label className={labelClass}>Новый пароль</label>
              <input
                type="password"
                value={newPassword}
                onChange={(e) => setNewPassword(e.target.value)}
                placeholder="Минимум 8 символов"
                className={inputClass}
                minLength={8}
                maxLength={128}
                required
              />
            </div>
            <div>
              <label className={labelClass}>Подтвердите новый пароль</label>
              <input
                type="password"
                value={confirmPassword}
                onChange={(e) => setConfirmPassword(e.target.value)}
                placeholder="••••••••"
                className={inputClass}
                maxLength={128}
                required
              />
            </div>
          </div>
          <div className="mt-4 sm:mt-6 flex flex-wrap items-center gap-3 sm:gap-4">
            <button
              type="submit"
              disabled={passwordSaving}
              className="px-4 sm:px-6 py-2 sm:py-3 rounded-lg sm:rounded-xl bg-[#3347ff] hover:bg-[#3347ff]/90 text-white text-xs sm:text-sm font-medium uppercase tracking-wider transition-colors disabled:opacity-50"
            >
              {passwordSaving ? 'Сохранение...' : 'Изменить пароль'}
            </button>
            {passwordError && <p className="text-sm text-red-400">{passwordError}</p>}
            {passwordSuccess && <p className="text-sm text-emerald-400">Пароль успешно изменён</p>}
          </div>
        </form>
      </section>

      {/* 2FA — карточка на всю ширину */}
      <section className="rounded-lg sm:rounded-xl bg-[#0a1635] border border-white/10 p-4 sm:p-6">
        <div className="mb-4 sm:mb-6">
          <h2 className="text-base sm:text-lg font-semibold text-white">Двухфакторная аутентификация (2FA)</h2>
          <p className="text-sm text-white/50 mt-0.5">Дополнительный уровень защиты при входе</p>
        </div>

        {twoFAError && (
          <div className="mb-6 p-4 rounded-xl bg-red-500/10 border border-red-500/20 text-red-400 text-sm">
            {twoFAError}
          </div>
        )}
        {twoFASuccess && (
          <div className="mb-6 p-4 rounded-xl bg-emerald-500/10 border border-emerald-500/20 text-emerald-400 text-sm">
            {twoFASuccess}
          </div>
        )}

        {profile?.twoFactorEnabled ? (
          <div className="space-y-6">
            <div className="p-4 rounded-xl bg-emerald-500/10 border border-emerald-500/20 text-emerald-400 text-sm">
              2FA включена. Ваш аккаунт защищён дополнительным кодом при входе.
            </div>
            <form onSubmit={handleDisable2FA} className="w-full">
              <p className="text-sm text-white/60 mb-4">Для отключения 2FA введите пароль и текущий код из приложения.</p>
              <div className="grid grid-cols-1 sm:grid-cols-2 gap-6 max-w-2xl">
                <div>
                  <label className={labelClass}>Пароль</label>
                  <input
                    type="password"
                    value={disable2FAPassword}
                    onChange={(e) => setDisable2FAPassword(e.target.value)}
                    placeholder="••••••••"
                    className={inputClass}
                    required
                  />
                </div>
                <div>
                  <label className={labelClass}>Код из приложения</label>
                  <input
                    type="text"
                    value={disable2FACode}
                    onChange={(e) => setDisable2FACode(e.target.value.replace(/\D/g, '').slice(0, 6))}
                    placeholder="000000"
                    className={`${inputClass} font-mono text-lg tracking-widest`}
                    maxLength={6}
                    required
                  />
                </div>
              </div>
              <button
                type="submit"
                disabled={disable2FALoading}
                className="mt-6 px-6 py-3 rounded-xl bg-red-500/20 hover:bg-red-500/30 text-red-400 text-sm font-medium uppercase tracking-wider transition-colors disabled:opacity-50 border border-red-500/30"
              >
                {disable2FALoading ? 'Отключение...' : 'Отключить 2FA'}
              </button>
            </form>
          </div>
        ) : step2FA === 'qr' ? (
          <div className="w-full space-y-6">
            <p className="text-sm text-white/70">
              Отсканируйте QR-код в приложении для аутентификации (Google Authenticator, Authy и др.):
            </p>
            <div className="flex flex-col sm:flex-row gap-8 items-start">
              {qrCode && (
                <div className="p-4 rounded-xl bg-white inline-block shrink-0">
                  <img src={qrCode} alt="QR-код" className="w-48 h-48" />
                </div>
              )}
              <div className="flex-1 min-w-0 space-y-6">
                {backupCodes.length > 0 && (
                  <div>
                    <p className="text-sm text-white/70 mb-2">Сохраните резервные коды в надёжном месте. Каждый код можно использовать один раз.</p>
                    <div className="flex flex-wrap gap-2">
                      {backupCodes.map((code, i) => (
                        <button
                          key={i}
                          type="button"
                          onClick={() => handleCopyBackupCode(code, i)}
                          className="flex items-center gap-2 px-3 py-2 rounded-lg bg-white/5 border border-white/10 text-white/90 font-mono text-xs hover:bg-white/10 transition-colors"
                        >
                          {code}
                          {copiedCode === i ? <Check className="w-3 h-3 text-emerald-400" /> : <Copy className="w-3 h-3 text-white/50" />}
                        </button>
                      ))}
                    </div>
                  </div>
                )}
                <form onSubmit={handleVerify2FA} className="space-y-4">
                  <div>
                    <label className={labelClass}>Введите код из приложения</label>
                    <input
                      type="text"
                      value={verifyCode}
                      onChange={(e) => setVerifyCode(e.target.value.replace(/\D/g, '').slice(0, 6))}
                      placeholder="000000"
                      className={`${inputClass} font-mono text-lg tracking-widest max-w-[200px]`}
                      maxLength={6}
                      required
                    />
                  </div>
                  <div className="flex gap-3">
                <button
                  type="submit"
                  disabled={verify2FALoading}
                  className="px-6 py-3 rounded-xl bg-[#3347ff] hover:bg-[#3347ff]/90 text-white text-sm font-medium uppercase tracking-wider transition-colors disabled:opacity-50"
                >
                      {verify2FALoading ? 'Проверка...' : 'Подтвердить'}
                    </button>
                <button
                  type="button"
                  onClick={cancel2FASetup}
                  className="px-6 py-3 rounded-xl bg-white/10 hover:bg-white/15 text-white text-sm font-medium uppercase tracking-wider transition-colors"
                >
                      Отмена
                    </button>
                  </div>
                </form>
              </div>
            </div>
          </div>
        ) : (
          <div className="flex flex-col sm:flex-row sm:items-center sm:justify-between gap-4 p-4 rounded-xl bg-white/[0.03] border border-white/[0.06]">
            <p className="text-sm text-white/60">
              Добавьте дополнительный уровень защиты. При входе потребуется код из приложения.
            </p>
            <button
              type="button"
              onClick={handleEnable2FA}
              disabled={enable2FALoading}
              className="px-6 py-3 rounded-xl bg-[#3347ff] hover:bg-[#3347ff]/90 text-white text-sm font-medium uppercase tracking-wider transition-colors disabled:opacity-50 shrink-0"
            >
              {enable2FALoading ? 'Загрузка...' : 'Включить 2FA'}
            </button>
          </div>
        )}
      </section>
    </div>
  );
}
